module OOP
  module Principles
    class Inheritance
      def define
        <<~DEFINE.strip
          Inheritance is the mechanism of basing an object or class
          upon another object or class, retaining similar implementation.
          This allows programmers to create classes that are built upon existing
          classes, to specify a new implementation while maintaining the same
          behaviors, to reuse code, and to independently extend original
          software via public classes and interfaces.
        DEFINE
      end
    end
  end
end
