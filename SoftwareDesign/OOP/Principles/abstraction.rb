module SoftwareDesign
  module OOP
    module Principles
      class Abstraction
        def define
          <<~DEFINE.strip
            Abstraction is the process of removing physical, spatial,
            or temporal details or attributes in the study of
            objects or systems in order to focus attention on
            details of higher importance.
          DEFINE
        end
      end
    end
  end
end
